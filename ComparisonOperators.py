""""
> Greater than
< lesser than
!= Not equal
>= Greater than or equal to
<= lesser than or equal to
== Equal to
"""

# > : Returns false if the statement isnt true and true if its true
val = 5
print(val > 3)
# result is true

# < : Returns false if the statement isnt true and true if its true
var = 5
print( var < 2 )
# result is false

# != : Returns false if the statement isnt true and true if its true
vap = 4
print(vap != 3)
# result is false

# >= : Returns false if the statement isnt true and true if its true
vape = 10
print(vape >= 10)
# result is true

# == : Returns false if the statement isnt true and true if its true
print(vape == vap)